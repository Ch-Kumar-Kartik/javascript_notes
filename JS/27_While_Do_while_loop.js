/*
while (condition) {
    // Code block to execute
    increment/decrement the variable
}
*/

let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}

// A do-while loop executes a block of code at 
// least once

/*
do {
    // Code block to execute
    increment/decrement
} while (condition);
*/

let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);